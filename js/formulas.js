/******************************************************
 * გაზის ნაკადის ფორმულები - პარამეტრების ერთეულები (SI ერთეულები)
 * * პარამეტრი | აღწერა                                            | ერთეული
 * ----------|------------------------------------------------------|--------
 * Q         | მოცულობითი ხარჯი, სტანდარტული პირობები               | მ³/წმ
 * E         | მილსადენის ეფექტურობა, მნიშვნელობა 1-ზე ნაკლები      | განზომილების გარეშე
 * Tb        | ბაზისური ტემპერატურა (ფუნქციაში გადადის K-ში)        | °C
 * Pb        | ბაზისური წნევა                                       | კპა
 * P1        | წნევა ნაკადის ზედა წერტილში                          | კპა
 * P2        | წნევა ნაკადის ქვედა წერტილში                          | კპა
 * G         | გაზის ფარდობითი სიმკვრივე (ჰაერი = 1)                | განზომილების გარეშე
 * Tf        | გაზის ტემპერატურა საშუალო პირობებში (გადადის K-ში)    | °C
 * Le        | მილის სეგმენტის ექვივალენტური სიგრძე                 | კმ
 * L         | მილის სეგმენტის სიგრძე                               | კმ
 * Z         | გაზის კუმშვადობის კოეფიციენტი                        | განზომილების გარეშე
 * D         | მილის შიდა დიამეტრი                                  | მმ
 * s         | სიმაღლის კორექტირების პარამეტრი                      | განზომილების გარეშე
 * H1        | ზედა წერტილის სიმაღლე                                | მ
 * H2        | ქვედა წერტილის სიმაღლე                                | მ
 * f         | დარსის ხახუნის კოეფიციენტი (მხოლოდ Darcy-Weisbach)    | განზომილების გარეშე
 * rho       | გაზის სიმკვრივე ნაკადის პირობებში (მხოლოდ Darcy-Weisbach) | კგ/მ³
 * * შენიშვნები:
 * - Weymouth: ზოგადი დანიშნულების გაზის ნაკადის ფორმულა
 * - Darcy-Weisbach: ფუნდამენტური ჰიდროდინამიკური მიდგომა
 * - Panhandle A: გამოიყენება რეინოლდსის რიცხვისთვის 5-11 მლნ, 12-60 ინჩიანი მილებისთვის
 * - Panhandle B: გამოიყენება რეინოლდსის რიცხვისთვის 4-40 მლნ, >36 ინჩიანი მილებისთვის
 ******************************************************/

function weymouth({ E, Tb, Pb, P1, P2, G, Tf, L, D, Z, H1, H2 }) {
  // ტემპერატურის გადაყვანა °C-დან K-ში
  const Tb_K = Tb + 273.15;
  const Tf_K = Tf + 273.15;

  // სიმაღლის კორექტირების პარამეტრი
  const s = 0.0684 * G * ((H2 - H1) / (Tf_K * Z));

  // ექვივალენტური სიგრძე (Le)
  let Le = L;
  if (Math.abs(s) > 1e-12) {
    Le = (L * (Math.exp(s) - 1)) / s;
  }

  // ვეიმუთის სრული ფორმულა
  const Q = 3.7435e-3 * (Tb_K / Pb) * Math.sqrt((Math.pow(P1, 2) - Math.exp(s) * Math.pow(P2, 2)) / (G * Tf_K * Le * Z)) * Math.pow(D, 2.667) * E / (24 * 60 * 60);

  return Q;
}

function darcyWeisbach({ E, Tb, Pb, P1, P2, G, Tf, L, D, Z, f, rho, H1, H2 }) {
  // ტემპერატურის გადაყვანა °C-დან K-ში
  const Tb_K = Tb + 273.15;
  const Tf_K = Tf + 273.15;
  
  // ერთეულების კონვერტაცია
  const D_m = D / 1000;  // მმ -> მ
  const L_m = L * 1000;  // კმ -> მ
  const P1_Pa = P1 * 1000;  // კპა -> პა
  const P2_Pa = P2 * 1000;  // კპა -> პა
  const Pb_Pa = Pb * 1000;  // კპა -> პა
  
  // წნევის ვარდნის დათვლა სიმაღლის ეფექტის გათვალისწინებით
  const g = 9.81;  // თავისუფალი ვარდნის აჩქარება
  const deltaPf = P1_Pa - P2_Pa - rho * g * (H2 - H1);  // წნევის ვარდნა ხახუნის გამო
  
  // სიჩქარის გამოთვლა დარსი-ვაისბახის ფორმულით: ΔPf = f * (L/D) * (ρ*v²/2)
  // გადაკეთებული: v = sqrt(2 * ΔPf * D / (f * L * ρ))
  const velocity = Math.sqrt((2 * deltaPf * D_m) / (f * L_m * rho));
  
  // განივი კვეთის ფართობის გამოთვლა
  const area = Math.PI * Math.pow(D_m, 2) / 4;
  
  // ნაკადის ფაქტობრივი მოცულობითი ხარჯის გამოთვლა
  const Q_actual = velocity * area * E;
  
  // გადაყვანა სტანდარტულ პირობებში
  const Q_standard = Q_actual * (P1_Pa / Pb_Pa) * (Tb_K / Tf_K) * Z;
  
  return Q_standard;
}

function panhandleA({ E, Tb, Pb, P1, P2, G, Tf, L, D, Z, H1, H2 }) {
  // ტემპერატურის გადაყვანა °C-დან K-ში
  const Tb_K = Tb + 273.15;
  const Tf_K = Tf + 273.15;

  // სიმაღლის კორექტირების პარამეტრი
  const s = 0.0684 * G * ((H2 - H1) / (Tf_K * Z));

  // ექვივალენტური სიგრძე (Le)
  let Le = L;
  if (Math.abs(s) > 1e-12) {
    Le = (L * (Math.exp(s) - 1)) / s;
  }

  // პანჰენდლი A ფორმულა (SI ერთეულები)
  // Q = 4.5965×10^-3 × (Tb/Pb)^1.0788 × ((P1^2 - e^s × P2^2)/(G^0.8539 × Tf × Le × Z))^0.5394 × D^2.6182 × E
  const Q_daily = 4.5965e-3 * Math.pow(Tb_K / Pb, 1.0788) * Math.pow((Math.pow(P1, 2) - Math.exp(s) * Math.pow(P2, 2)) / 
                  (Math.pow(G, 0.8539) * Tf_K * Le * Z), 0.5394) * Math.pow(D, 2.6182) * E;

  // გადაყვანა მ³/დღე-ღამე-დან მ³/წმ-ში
  const Q = Q_daily / (24 * 60 * 60);

  return Q;
}

function panhandleB({ E, Tb, Pb, P1, P2, G, Tf, L, D, Z, H1, H2 }) {
  // ტემპერატურის გადაყვანა °C-დან K-ში
  const Tb_K = Tb + 273.15;
  const Tf_K = Tf + 273.15;

  // სიმაღლის კორექტირების პარამეტრი
  const s = 0.0684 * G * ((H2 - H1) / (Tf_K * Z));

  // ექვივალენტური სიგრძე (Le)
  let Le = L;
  if (Math.abs(s) > 1e-12) {
    Le = (L * (Math.exp(s) - 1)) / s;
  }

  // პანჰენდლი B ფორმულა (SI ერთეულები)
  // Q = 1.002×10^-2 × (Tb/Pb)^1.02 × ((P1^2 - e^s × P2^2)/(G^0.961 × Tf × Le × Z))^0.51 × D^2.53 × E
  const Q_daily = 1.002e-2 * Math.pow(Tb_K / Pb, 1.02) * Math.pow((Math.pow(P1, 2) - Math.exp(s) * Math.pow(P2, 2)) / 
                  (Math.pow(G, 0.961) * Tf_K * Le * Z), 0.51) * Math.pow(D, 2.53) * E;

  // გადაყვანა მ³/დღე-ღამე-დან მ³/წმ-ში
  const Q = Q_daily / (24 * 60 * 60);

  return Q;
}

